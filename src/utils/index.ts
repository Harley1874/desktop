/* 大写转小写
 * @param {String} str
 * @return {String} str
 * @example
 * toLowerCase('ABC') => 'abc'
 * toLowerCase('aBc') => 'abc'
 * toLowerCase('abc') => 'abc'
 */
export function toLowerCase(str: string) {
  // 大写A转小写a
  return str.toLowerCase();
}
